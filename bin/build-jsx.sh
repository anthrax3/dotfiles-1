#!/bin/bash
. "`dirname -- "$0"`"/functions.sh || exit

src="$DOTFILES_DIR/env/src/jsx"
dest="$DOTFILES_DIR/env/dest/jsx"

# get sources
if [ ! -d "$src/.git" ]; then
  git clone https://github.com/jsx/JSX.git "$src" || exit 1
fi
cd "$src" || exit 1
git pull

# install
make setup || exit 1
ln -sfn "$src" "$dest"

# create startup script
( echo "# generated by $DOTFILES_DIR/bin/$(basename -- "$0")"
  echo "export PATH=\"$dest/bin:\$PATH\""
) | make_profile_script 10-jst.sh

